class Board

  @@black_board_state = {'♔' => ['e8'], '♕' => ['d8'], '♖' => ['a8', 'h8'], '♗' => ['c8', 'f8'], '♘' => ['b8', 'g8'], '♙' => ['a7', 'b7', 'c7', 'd7', 'e7', 'f7', 'g7', 'h7']}

  @@white_board_state = {'♚' => ['e1'], '♛' => ['d1'], '♜' => ['a1', 'h1'], '♝' => ['c1', 'f1'], '♞' => ['b1', 'g1'], '♟' => ['a2', 'b2', 'c2', 'd2', 'e2', 'f2', 'g2', 'h2']}

  @@board_spaces = {
    'a' => ['♜', '♟', ' ', ' ', ' ', ' ', '♙', '♖'],
    'b' => ['♞', '♟', ' ', ' ', ' ', ' ', '♙', '♘'], 
    'c' => ['♝', '♟', ' ', ' ', ' ', ' ', '♙', '♗'], 
    'd' => ['♛', '♟', ' ', ' ', ' ', ' ', '♙', '♕'], 
    'e' => ['♚', '♟', ' ', ' ', ' ', ' ', '♙', '♔'], 
    'f' => ['♝', '♟', ' ', ' ', ' ', ' ', '♙', '♗'], 
    'g' => ['♞', '♟', ' ', ' ', ' ', ' ', '♙', '♘'], 
    'h' => ['♜', '♟', ' ', ' ', ' ', ' ', '♙', '♖']
  }

  def initialize()

  end

  def create_board
    board = "\n╔═╤═╤═╤═╤═╤═╤═╤═╗╮\n║"

    7.downto(0) do |i|
      @@board_spaces.each_key do |k|
        if k == 'h'
          board += @@board_spaces[k][i]
        else
          board += @@board_spaces[k][i] + '│'
        end
      end
      if i == 0
        board += '║' + (i + 1).to_s
      else
        board += '║' + (i + 1).to_s + "\n╟─┼─┼─┼─┼─┼─┼─┼─╢┊\n║"
      end
    end

    board += "\n╚═╧═╧═╧═╧═╧═╧═╧═╝┊\n╰a┈b┈c┈d┈e┈f┈g┈h┈╯\n"
  end

end