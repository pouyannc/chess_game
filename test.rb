   'h' => ['♜', '♟', ' ', ' ', ' ', ' ', '♙', '♖']}

def create_board(hash)
  board = "\n╔═╤═╤═╤═╤═╤═╤═╤═╗╮\n║"

  7.downto(0) do |i|
    hash.each_key do |k|
      if k == 'h'
        board += hash[k][i]
      else
        board += hash[k][i] + '│'
      end
    end
    if i == 0
      board += '║' + (i + 1).to_s
    else
      board += '║' + (i + 1).to_s + "\n╟─┼─┼─┼─┼─┼─┼─┼─╢┊\n║"
    end
  end

  board += "\n╚═╧═╧═╧═╧═╧═╧═╧═╝┊\n╰a┈b┈c┈d┈e┈f┈g┈h┈╯\n"
end

